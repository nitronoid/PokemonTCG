#ifndef CARDS_H
#define CARDS_H


class Cards
{
public:
    ///-----------------------------------------------------------------
    /// @build constructor
    ///-----------------------------------------------------------------
    Cards();
    ///-----------------------------------------------------------------
    /// @build set type function
    ///-----------------------------------------------------------------
    void setType(int _type);
    ///-----------------------------------------------------------------
    /// @build set health function
    ///-----------------------------------------------------------------
    void setHealth(int _health);
    ///-----------------------------------------------------------------
    /// @build set energy for attack 1
    ///-----------------------------------------------------------------
    void setEnergy0(int _energy);
    ///-----------------------------------------------------------------
    /// @build set energy for attack 2
    ///-----------------------------------------------------------------
    void setEnergy1(int _energy);
    ///-----------------------------------------------------------------
    /// @build set attack amount 1
    ///-----------------------------------------------------------------
    void setAttack0(int _attack);
    ///-----------------------------------------------------------------
    /// @build set attack amount 2
    ///-----------------------------------------------------------------
    void setAttack1(int _attack);
    ///-----------------------------------------------------------------
    /// @build set current energy function
    ///-----------------------------------------------------------------
    void setCurrentEnergy(int _currentEnergy);
    void addEnergy();
    ///-----------------------------------------------------------------
    /// @build get type function
    ///-----------------------------------------------------------------
    int getType();
    ///-----------------------------------------------------------------
    /// @build get health function
    ///-----------------------------------------------------------------
    int getHealth();
    ///-----------------------------------------------------------------
    /// @build get energy function
    ///-----------------------------------------------------------------
    int getEnergy_0();
    int getEnergy_1();
    ///-----------------------------------------------------------------
    /// @build get attack amounts function
    ///-----------------------------------------------------------------
    int getAttack_0();
    int getAttack_1();
    ///-----------------------------------------------------------------
    /// @build get current energy function
    ///-----------------------------------------------------------------
    int getCurrentEnergy();
private:
    ///-----------------------------------------------------------------
    /// @build type
    /// 0 basic pokemon, 1 is stage1, 2 is stage2
    ///-----------------------------------------------------------------
    int m_type;
    ///-----------------------------------------------------------------
    /// @build health
    ///-----------------------------------------------------------------
    int m_health;
    ///-----------------------------------------------------------------
    /// @build energy needed for attacks
    ///-----------------------------------------------------------------
    int m_energy_0;
    int m_energy_1;
    int m_currentEnergy;
    ///-----------------------------------------------------------------
    /// @build amount of two attacks
    ///-----------------------------------------------------------------
    int m_attack_1;
    int m_attack_2;
};

#endif // CARDS_H
