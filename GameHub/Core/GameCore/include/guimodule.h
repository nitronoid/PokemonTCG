#ifndef GUIMODULE_H
#define GUIMODULE_H
#include "board.h"

class Game;

class GuiModule
{
public :
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief default ctor for GuiModule
  //----------------------------------------------------------------------------------------------------------------------
  GuiModule() = default;
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief dtor for GuiModule
  //----------------------------------------------------------------------------------------------------------------------
  virtual ~GuiModule();
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief method for drawing the board
  /// @return none
  //----------------------------------------------------------------------------------------------------------------------
  virtual void drawBoard() = 0;
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief method for sourcing the game
  /// @param [in] _subject source game
  /// @return none
  //----------------------------------------------------------------------------------------------------------------------
  void setGame(Game*const _subject);
protected:
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief source game
  //----------------------------------------------------------------------------------------------------------------------
  Game* m_subject = nullptr;
};

#endif // GUIMODULE_H
