#ifndef GUIMODULE_H
#define GUIMODULE_H
#include "board.h"

class Game;

class GuiModule
{
public:
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief Event enum to communicate what has happened
  //----------------------------------------------------------------------------------------------------------------------
  enum class Event { START_TURN, EFFECT_USED, MOVE_CARD, KNOCK_OUT };
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief default ctor for GuiModule
  //----------------------------------------------------------------------------------------------------------------------
  GuiModule() = default;
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief default virtual dtor for GuiModule
  //----------------------------------------------------------------------------------------------------------------------
  virtual ~GuiModule();
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief interface for drawing the board
  //----------------------------------------------------------------------------------------------------------------------
  virtual void drawBoard(const Event _event) = 0;
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief method for sourcing the game to be observed
  /// @param [in] _subject source game to observe
  //----------------------------------------------------------------------------------------------------------------------
  void setGame(Game*const _subject);

protected:
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief pointer to the source game to observe
  //----------------------------------------------------------------------------------------------------------------------
  Game* m_subject = nullptr;
};

#endif // GUIMODULE_H
